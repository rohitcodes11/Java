public class AssignmentsOperators{
    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        a += 5;
        System.out.println(a);

        b -= 3;
        System.out.println(b);

        a *= 2;
        System.out.println(a);

        a /= 4;
        System.out.println(a);

        a %= 3;
        System.out.println(a);
    }
}

