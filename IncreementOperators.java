// post incrementand decrement operator
public class IncreementOperators{
    public static void main(String[] args) {

        int a = 5;

        System.out.println(a++);
        System.out.println(++a);

        int b = 5;

        System.out.println(b--);
        System.out.println(--b);
    }
}