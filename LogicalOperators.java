  // Logical Operators

public class LogicalOperators {
    public static void main (String[]args){
        System.out.println(10 > 5 && 20 > 10);
        System.out.println(10 > 5 && 20 < 10);

        System.out.println(10 > 5 || 2 > 8);
        System.out.println(3 > 8 || 1 > 5);

        System.out.println(!(10 > 5));
        System.out.println(!(5 > 10));
    }
}
